class  Switch
{
public static void main(String a[])
{
int number=1;
switch(number)
{
   case 1:
      System.out.print("one");
      break;
   case 2: 
      System.out.print("two");
      break;
   case 3:
      System.out.print("three");
      break;
   case 4:
      System.out.print("four");
      break;
   case 5:
      System.out.print("five");
      break;
   case 7:
      System.out.print("seven");
      break;
   default:
      System.out.print("invalid input");
}
}
}






